<div class="row">
	<div class="span18">
		<h3>Der Dampfende Dschungel von Gorn</h3>		
	</div>
</div>

<div class="row">
	<div class="span9">
		<p>Von Trondheim aus fuhr ich an Bord einer tiefländischen Händlerkogge über das Nordmeer, vorbei an dem Land der Orks. Ein Glück, dass diese nichts vom Schiffsbau verstehen. In Bram gingen wir noch einmal an Land, um Vorräte aufzustocken und brachen dann auf nach Belinga. Wir fuhren über den großen Ozean, um das Südliche Kap herum in die Südsee. Dort entkamen wir Seemonstern, konnten einen Angriff von Piraten abwehren und gelangten schließlich mit wenigen Verlusten in Belinga an.</p>
		<p>Belinga ist eine Stadt im Sumpf, welche auf hölzerne Stelzen gebaut wurden. Die Häuser sind umflossen vom stinkenden Brackwasser und man kann sich über weite Strecken nur mit dem Boot fortbewegen. Noch schlimmer als der Gestank sind die menschlichen Abgründe die sich hier auf tun: Hehlerei und Hurerei, Spielsucht und Betrug überall. Das Imperium scheint nicht in der Lage dies auch nur ansatzweise zu unterbinden. Auch scheinen die Götter hier im dampfenden Urwald blind zu sein.</p>
	</div>

	<div class="span9">
		<p>Belinga ist ein Anziehungspunkt für Abenteurer und Goldgierige, solche die dem schnellen Leben frönen. Im östlich gelegenen Gorgergebirge sollen riesige Mengen Gold gefunden worden sein. Doch die starken magischen Strömungen in diesem Gebiet und die gefährlichen Bestien dort, machen den Abbau nicht gerade einfach. Es soll sogar vor einiger Zeit eine Gruppe Zwerge in das Gorgergebirge gezogen sein. Allesamt sollen sie vom Chaos korrumpiert worden sein, und nur noch wahnsinnige sabbernde gestalten sein. </p>
		<p>Westlich von Belinga leben die Echsen, die gerne Menschen fangen und ihren heidnischen Göttern opfern. Dies macht das Leben hier auch nicht gerade einfacher. Auch sollen noch weiter westlich Gebiete liegen, in die sich selbst diese Echsenmenschen nicht hineintrauen. Öliger teeriger Sumpf ohne Durchkommen und Überleben.</p>
	</div>
</div>
	
