		<h3>Keldon, das Land im hohen Norden</h3>	
	</div>
</div>

<div class="row">
	<div class="span9">
		<p>In westlichen Norden unseres schönen Kontinents Ult liegt das Land Keldon. Über Land kann man dorthin nur durch die Stosteppe gelangen, welche von den schrecklichen Orks bewohnt ist, oder über den eisigen Holmpass. Der Pass führt durch das Nebelgebirge, gefährlich nah vorbei an der Sturmspitze, dem Land der Schattenelfen. Nur allein die Zwerge wissen um einen halbwegs sicheren Weg durch das Gebirge.</p> 
		<p>Der Seeweg über den Großen Ozean und das Nordmeer ist nur im Sommer von den Schiffen befahrbar. Selbst dann ist es nur für die erfahrensten Kapitäne möglich, an den steinigen Klippen des rauen Landes entlang zu manövrieren.</p>
		<p>Mein Weg führte mich daher über den Holmpass vorbei an Holmstedt die letzte Bastion meines geliebten Imperiums und auf in den eisigen Norden. Er führte mich durch den Norforst: Einem schier endlos erscheinenden dunklen Nadelwald, bewohnt von noch wilderen Orks, als solche, die man aus der Stosteppe kennt.</p>	
	</div>

	<div class="span9">
		<p>Auch musste ich mich vor den Schattenelfen der Sturmspitze in acht nehmen, die Reisende nur zu gerne fangen und wissen die Götter was mit ihnen anstellen. Ich hielt mich östlich und so war das erste Anzeichen von Zivilisation nach einer langen und kalten Reise die keldonische Siedlung Glomfjord an der Küste des Ijsmaares. Glomfjord wird von dem Clan der MacLeans geführt. Nördlich dieser Siedlung liegt Estwingen, ebenfalls am Ijsmaar, wo die O`Mulls die Führung haben.</p>
		<p>Weiter westlich an der Nordmeerküste, dort wo der Fluss Fjolak mündet, liegt die größte Siedlung der Keldonen – Trondheim. Trondheim ist der einzige Überseeweg zum Imperium, da die Wasserwege nach Estwingen und Glomfjord durch das immerwährede Packeis unmöglich zu befahren sind. Die Führung der Keldonen wechselt ständig, wobei Grodrig Mac`Fadden und Fingolf O`Grom mit ihren jeweiligen Anhängern leidenschaftlich konkurrieren. Dies artet nicht selten in handfesen Schlägerreien aus.</p>
	</div>
</div>
	
