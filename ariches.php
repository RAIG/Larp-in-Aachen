		<h3>Das Glorreiche Imperium</h3>
	</div>
</div>
	
<div class="row">
	<div class="span9">
		<p>Das Imperium besteht aus fünf Fürstentümern, die von Ulrich dem Ersten vor über 800 Jahren geeint wurden. Rodach ist seit den Gründungstagen die Hauptstadt des Imperiums und gleichzeitig der Provinz Raigland. Sie liegt am Kynraigsee, dort wo der Niederraig weiterfließt.</p>
		<p>Nördlich des Raiglandes liegt getrennt durch die Hornberge die Stosteppe, wo sich die wilden Orks tummeln. Das Imperium wird vom Tyrnogwall gegen diese dämonischen Bestien beschützt, einer Wallanlage die den Gebirgspass versperrt.</p>
		<p>Wenn man den Lauf des Raigs flussabwärts verfolgt, kommt man in die blühende Handelsstadt Bram am Großen Ozean, die der Sitz des Herzogs von Westtiefland und der Seefahrergilde ist.</p>
		<p>Südlich durch den Altwald gelangt man weiter in die Grafschaft Moanauen, die der Fluss Urs durchfließt. Die Grafschaft wird südlich durch die Erzberge abgetrennt, dahinter erstrecken sich die dampfenden Dschungel von Gorn. Die Hauptstadt der Grafschaft ist Erkester, welche über die Furt über den Urs wacht. Verfolgt man dem Urs flussaufwärts gelangt man schließlich nach Hammerheim, die eiserne Krone der imperialen Rüstungsindustrie.</p>
	</div>

	<div class="span9">
		<p>Weiter östlich gelangt man in die Provinz Trakland, welches früher von den wilden Thrakern bewohnt wurde. Die Reichsritter gründeten im westlichen Teil vor über 300 Jahren die Festung Trakenburg, um die gottlosen Thraker mit Feuer und Schwert die unbestreitbare Gerechtigkeit Theros beizubringen. Auch heute gibt es noch Thraker, doch diese sind heimatlos und ziehen meistens mit Zigeunern und Gauklern durchs Land. Die Hauptstadt des Fürstentums Thrakland ist Boerlangen. Sie liegt auf einer Anhöhe in dem ansonsten flachen und nur von Gras bewachsenen Umland und bietet dadurch sowohl einen guten Orientierungspunkt für Reisende, als auch einen guten Aussichtsposten. Südlich grenzt das Fürstentum an das Rotgebirge, wohinter die entsetzlich lodernde Drauwüste liegt, und östlich an den unendlich scheinenden Nebelwald, welcher von den Waldelfen bewohnt wird. Kaum ein Mensch hat diesen Wald betreten und die wenigen, die das Glück hatten Lebend wieder aus diesem heraus zu finden, waren meist wahnsinnig und verstört.</p>
		<p>Folgt man der Straße von Boerlangen aus nach Norden, so erreicht man schließlich die Ostmark sowie deren Hauptstadt Neremund, welches an einer Furt über den Raig liegt. Weiter nördlich liegt das Kloster Hoderung, berühmt für seine große Bibliothek und dem gut gekelterten Wein. Nördlich der Ostmark liegt das steinerne Reich der Zwerge, die sich tief in den Dunpad-Berg gegraben haben.</p>
	</div>
</div>

