		<h3>Die brennende Drauwüste</h3>		
	</div>
</div>

<div class="row">
	<div class="span9">	
		<p>Nach einiger Zeit in Belinga stachen wir endlich wieder in See, raus aus diesem stinkenden Loch. Wir verließen die Südsee in Richtung Gul´Ata über das Feuermeer. Gul´Ata selbst ist eine beeindruckende Stadt voller Leben. Von der Geschäftigkeit her, ist die Stadt mit Bram zu vergleichen. Gerade aus der Ferne sehen die eckigen Häuser mit ihrem kalkweißen Putz beinahe majestätisch aus. Ich hatte das Glück, an einem Basartag anzureisen und so konnte ich schnell eine Karawane finden, die mich mitnehmen konnte zu den Oasen Al`Kharga und Al`Baric. Diese liegen in Richtung der alten Ruinen der mystischen Stygier.</p>
		<p>Leider muss ich wohl meine Börse irgendwo auf dem dicht gedrängten Basar verloren haben, ein Glück das ich nie alles mitnehme. Nach mehreren Tagen rastloser Wanderschaft auf dem Rücken eines schwankenden Kamels, durch die brennende Hitze der Drauwüste, sah ich zum ersten Mal die alten Pyramiden, wo die Pharaonen des uralten Volkes ihren ewigen Schlaf schlafen. Halb verschüttet unter Sand und Staub und weit abseits von jeglichen Oasen.</p>	
	</div>

	<div class="span9">
		<p>Man sagt, dass die Magie dieses Volkes zu mächtig wurde und sie daher von den Göttern vor tausenden von Jahren für ihren Hochmut ausgelöscht wurden. Auf dem Rückweg zu der Wüstenperle Gul`Ata wurden wir von Aufständigen Drau angegriffen. Unter starken Verlusten konnten wir sie zurückschlagen. Kurz darauf überraschte uns jedoch ein Sandsturm, welchen nur ich und mein kleiner Führer Mum`ba dank Theros unendlicher Gnade überlebten. Ich war überglücklich als wir zurück waren in der weißen Stadt Gul`Ata, mit ihren schattenspendenden Palmen dem kühlenden Wasser und der Sicherheit, welche nur eineImperiale Stadt bieten können.</p>	
		<p>Nach einer weiteren beschwerlichen und mühseligen langen Reise legten wir endlich vor Bram an. von hier aus, ging es mit einem kleineren Flussboot weiter Richtung Rodach. Nie kamen mir die Länder des Raiglandes so grün und saftig vor, die Wälder so schön und das Klima so angenehm wie bei dieser Ankunft!</p>
	</div>
</div>

