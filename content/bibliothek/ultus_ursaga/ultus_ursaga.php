	<p>Ultus Ursaga ist eine Fantasy Rollenspielwelt und dient sowohl als Hintergrund für Pen and Paper Abenteuer, als auch für LARP Gruppen.</p>
	<p>Die Welt, in der das System spielt, hat eine flache runde Oberfläche, wenn auch so manch törichter Mensch behauptete, sie sei eine Kugel. Bereits mehrere Male wurde bereits das Gegenteil bewiesen, indem tapfere Seefahrer weit in Richtung Süden segelten und nie wieder auftauchten.</p>
	<p>Nach unten hin verjüngt sich die Welt zu einer Spitze. Von außen betrachtet, sieht die Welt daher wie ein Splitter aus, der aus einem kugelförmigen Planeten herausbrochen wurde.</p>
	<p>Am Rande der Oberfläche ragt das Weltrandgebirge hervor, stellenweise durchbrochen von mächtigen Wasserfällen die aus den Ozeanen gespeist in die unendlichen Tiefen stürzen. In der Mitte der Scheibe steigt ein riesiger spitzer Berg, beinahe unendlich hoch über die Wolken auf. Ob diese Weltenspitze der Sitz der guten Götter ist, ist bisher jedoch noch ungeklärt.</p> 
</div>

<div style="float:right; width:49%;">	
	<p>Über die gesamte Oberfläche sind vier Kontinente verteilt, von denen jedoch nur der Kontinent Ult bekannt ist und bespielt wird. Das Wissen über die anderen Kontinente beschränkt sich auf Weissagungen, Prophezeiungen, uralte mystische Aufzeichnungen und Spekulationen. Lediglich einige Keldonen behaupteten, sie wären am gefährlichen Ijsmaar vorbei in eine unbekannte Welt gefahren, die reichlich grün bewachsen aber völlig unbesiedelt gewesen seien.</p>
	<p>Da die Sonne in einer besonderen Umlaufbahn um die Oberfläche kreist, wird es vom Mittelpunkt her, zu den Außenrändern der Welt hin, immer heißer. Es wird spekuliert, dass man es nicht einmal schaffen würde, von der Oberfläche herunter zu fallen: Die Sonne hätte einen vorher bereits zu Asche verbrannt.</p>
