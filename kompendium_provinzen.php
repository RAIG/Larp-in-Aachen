		<h3>Die Moanauen</h3>
	</div>
</div>
	
<div class="row">
	<div class="span7">
		<p>Erkester ist der Sitz des Grafen der Moanauen und damit Hauptstadt der Grafschaft. Die Stadt wurde an einer Furt über den Urs erbaut und bildet so einen
		der wichtigsten Handelsknoten im Imperium. Berühmt ist Erkester aber vielmehr für seine im Jahre 12.921 ae. gegründete Militärakademie und für die dort ausgebildeten
		Infanteriekämpfer, sowie der Erkester Bihandkämpfer. Außerdem steht dort das Ordenshaus der Schildschwestern, welches 12.439 ae. dort eingerichtet wurde,
		sowie die größte Raya Kirche des Landes. Die Stadt ist schwer befestigt und einer der wenigen dauerhaften Standorte des Imperialen Heeres. In der Stadt selber gibt
		es viele berühmte Rüst,- sowie Waffenläden, da sich die Reicheren, welche sich eine Ausbildung an der dortigen Akademie leisten können, auch meistens ihre eigene
		Ausrüstung kaufen.</p>
		
		<p>Flett ist ein kleines landwirtschaftliches Örtchen zwischen den Erzbergen und dem Fluss Urs. Die wenigen ansässigen Einwohner sind zumeist Bauern, die
		die größere Stadt Erkester versorgen. Da sich hier kaum ein Fremder hin verirrt, gibt es kaum ein Angebot von exotischen Waren. Wenn Gefahren kommen, dann fast
		ausschließlich aus den Erzbergen.</p>
	</div>
	
	<div class="span7">
		<p>Hammerheim ist die Imperiale Rüstungskrone. Die kleine Stadt ist in den Erzbergen nur zu dem Zweck der Stahlgewinnung vom Hochkaiser Lothar III im Jahre
		der Götter 12382 ae. gegründet worden und wurde seither immer weiter ausgebaut. Der Urs ist der wichtigste Energielieferant, da er die unzähligen Hammermühlen antreibt,
		welche den Stahl schmieden. Das Erz wird vor Ort in den tiefen Minen der Erzberge abgebaut. Da nicht genügend Kohle zur verfügung steht, wird Holzkohle verwendet. Früher
		konnte diese vor Ort hergestellt werden, doch inzwischen sind die Wälder im Erzgebirge abgeholzt und so muss die Holzkohle aufwändig aus den Köhlereien am Rande des
		Altwaldes oder des Mooswaldes nach Hammerheim geschafft werden. Einen anderen Grund nach Hammerheim zu kommen gibt es wohl kaum. Die Gefahren der Erzberge,
		Goblinüberfälle und gelegentliche Trollbesuche wissen die Besucher aber inzwischen recht gut abzuwehren. Daher sterben mehr Leute durch Arbeitsunfälle und Steinschlag.</p>
	</div>
