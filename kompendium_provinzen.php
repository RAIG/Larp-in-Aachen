		<h3>Das Westtiefland</h3>
	</div>
</div>
	
<div class="row">
	<div class="span7">
		<p>Bram ist der Sitz des Herzogtums Westtiefland. Die florierende Handelsstadt liegt an der Mündung des Raigs in den Großen Ozean. Die
		Haupteinnahmequelle ist der Umschlag und Handel von Wahren zwischen den hochseetauglichen Schiffen und den kleineren Flussbooten, welche Rodach erreichen können. Dabei
		wird beinahe alles verschifft: Von Gold und edlen Hölzern aus Belinga, Silber und Schmucksteine aus Gul`Ata oder Pelze aus Trondheim, bis zu eigenen Produkten wie feinen
		Tüchern aus dem Tiefland. Bram ist ebenfalls der Sitz der Seefahrergilde, welche Zölle und Einfuhrregelungen für den Handel mit Schiffen festlegen. Außerdem stechen von 
		hier aus die einzigen beiden Kriegsschiffe des Imperiums in See, im Kampf gegen Piraterie. Unter den Westtiefländern gibt es viele blonde und groß gewachsene Menschen, da 
		sich schon seit mehreren Jahrhunderten vereinzelt Keldonen in diesem Gebiet ansiedeln. Gerade wegen ihrer meist stattlichen Statur und ihrem starken Ehrgefühl, werden 
		Westtiefländer gerne für die Hohe Wache des Imperators rekrutiert.</p>
	</div>

	<div class="span7">
		<p>Wettersted ist ein kleines Fischerdorf, welches in der Bucht direkt gegenüber von Bram liegt. Sollte Bram weiter wachsen, wird Wetterstedt eines Tages 
		von der Hauptstadt adaptiert werden.</p>
	
		<p>Altbruch liegt nahe des Altwaldes, auf dem Weg zwischen Bram und Erkester. Entsprechend häufig kommen Händler und sonstige Reisende durch die kleine Stadt.
		Die Herbergen und Tavernen sind immer gut gefüllt, doch die Haupt- einnahmequelle der Stadt sind die Forstbestände des Altwaldes. Zahlreiche Holzfäller und Köhler verarbeiten
		die Bäume aus dem Wald für die Brennöfen im Süden des Landes. Außerdem beherbergt Altbruch zahlreiche Jäger, die in großen Treibjagden Wild im Altwald erlegen und mit dem Fleisch
		Westtiefland und die nahe Provinz Raigland beliefern. Diese Arbeit ist jedoch nicht ungefährlich, da viele gefährliche Kreaturen wie Goblinbanden und andere wilde Tiere 
		im Wald leben. Altbruch selbst wird jedoch von einer äußerst starken Stadtmauer und einer gut ausgebildeten Stadtmiliz beschützt, sodass die Einwohner relativ sicher sind.</p>
	</div>
