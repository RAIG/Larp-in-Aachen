		<h3>Das Trakland</h3>
	</div>
</div>
	
<div class="row">
	<div class="span7">
		<p>Boerlangen ist der Mittelpunkt und Hauptsitz des Traklandes, welches das flächenmäßig Größte im Imperium ist. Boerlangen liegt auf einer kleinen Hügelkette 
		in mitten eines unendlich scheinenden Grasmeeres. Damit hat die Stadt eine herausragende Position zum verteidigen und kann auch noch von weitem als Landmarke benutzt 
		werden. Auf den Hügeln um Boerlangen wird der, im ganzen Imperium geliebter Trakländer Wein angebaut. Der Rest der zumeist grasbewachsenen Fläche des Landes wird zur 
		Vieh-, aber vor allem zur Pferdezucht genutzt. Außerdem liegt Boerlangen an der einzigen Karawanenverbindung in die Drauwüste, welche hier in der Gegend Wasser, Wein, 
		Früchte und Nahrung gegen Silberschmuck, Seide und Pfeigen tauschen. Die Stadt selbst ist größtenteils hell verputzt und auch innerhalb der Stadtmauern wachsen 
		allerortens Weinreben an den Wänden hinauf. Die einzigen besonderen Gebäude sind einmal natürlich der Hof des Herzogs und zum andern der für das Imperrium snst untypische 
		Schrein des Nidors. Früher gehörte das Land den Thrakern einem Stamm von Barbaren, welche ausschließlich Nidor als Schlachtengott anbeteten, doch diese wurden nun fast 
		vollständig eingegliedert in die herausragende Gesellschaft des Imperiums. Nur die Pferdezucht in diesem Gebiet erinnert noch an sie.</p>
	</div>

	<div class="span7">
		<p>Das Städtchen Moosbach liegt an der Moos, einem kleinen Flüsschen welches in den Moossee fließt. Die wenigen Leute, die hier in der inzwischen 
		beschaulichen Gegend wohnen, leben zu gleichen Teilen von der Landwirtschaft, der Fischerei wie auch als Köhler im nahe gelegenen Mooswald für die nie stillstehenden 
		Hochöfen in Hammerheim.</p>
		
		<p>Dronhysen ist eine Waldarbeitersidlung östlich von Boerlangen. Dabei Wagen sich die Waldarbeiter gefährlich nahe an den Nebelwald. Immer wieder werden dort
		schaurige Geschichten von Irrlichtern und Ammenmärchen von ähnlichen Kreaturen erzählt, die einen zu tief in den Wald hinein locken. Tatsache ist aber, dass die Einwohner 
		in der Tat die größte Wahrscheinlichkeit haben, Elfen oder Halblinge und sonstige Kobolde zu sehen.</p>
		
		<p>Die Trakenburg ist die Ordensburg der Reichsritter, einem Paladinorden des Thero und damit Sitz des Hochmeisters des Ordens. Sie wurde während der ersten 
		Thrakerkriege im Jahre 12.684 gegründet, um das Reich vor einem Einfall der Wilden aus dem Hinterland zu schützen und um die Thraker mit dem Schwert zu bekehren, da sie 
		nur Nidor als einzig wahren Gott ansahen und unseren Herren Thero und unserem Imperator Ulrich dem Ersten keinen Respekt zollten.</p>
	</div>
