		<h3>Das Raigland</h3>
	</div>
</div>
	
<div class="row">
	<div class="span7">
		<p>Der Tyrnogwall ist eine Festungsanlage, die den Pass durch die Hornberge zur Stosteppe versperrt. Im Tyrnogwall ist ein
		Großteil des stehenden Imperialen Heeres zur Bewachung des riesigen Tores untergebracht. In Auftrag gegeben wurde die Wallanlage von Ulrich dem Ersten, nachdem dieser
		abermals die Orks aus dem Reich getrieben hatte. Die Fertigstellung der Wallanlage fand aber erst 20 Jahre nach dem Tode Ulrichs statt, und wurde seither aber immer weiter
		ausgebaut. Zuletzt wurde der Wall vor 20 Jahren zu seiner heutigen Pracht vom jetzigen Kaiser Johann Hammerstein VII ausgebaut.</p>
		
		<p>Auch wenn Bram eigentlich Küstenstadt und Handelsmetropole ist, finden sich in Rodach trotzdem viele ausländische Reisende und Händler, wie Karawanenführer aus der Drauwüste,
		zwergische Metallhändler, Halblinge, Keldonische Pelzhändler, ja manchmal sogar waldelfische Botschaftsdelegationen ein. So viele unterschiedliche Völker und Händler zieht
		natürlich auch die Illegalität an. So ist Rodach, wenn man Belinga einmal außen vor lässt, da hier Korruption ja schon Normalität ist, die Stadt mit der höchsten
		Verbrechensrate. Die ortsansässige Stadtwache versucht dies natürlich so gut es geht in den Griff zu bekommen, aber bei solch einer großen Stadt, mit ihren vielen
		Verstecken, ist dies natürlich schwer. Die Imperiale Armee ist nicht in der Stadt stationiert, sondern hat ihren stehenden Teil Soldaten in einigen verstreuten
		Festungen stationiert.</p>
	</div>

	<div class="span7">
		<p>Rodach ist die Hauptstadt des Reiches. Neben dem Palast und der Festung von Rodach, die um 12270 ae von Ulrich dem Ersten erbaut worden ist, gibt es noch weitere
		Sehenswürdigkeiten, wie die Bibliothek und die Universität der Donnersturm Gilde, Theros Kathedrale und die große Arena zu Kros, nach ihrem Erbauer benannt und seit
		jeher Schauplatz der blutigsten Kämpfe zur Unterhaltung der Gemeinen.</p>
		
		<p>Bitterfels ist eine zentral im fruchtbaren Raigland gelegene Ortschaft. Sie ist mitverantwortlich für die Nahrungsversorgung von Rodach. Ihr Wahrzeichen ist
		dementsprechend die Windmühle, die das Bild der Region stark prägen. Aufgrund der Nähe zur Hauptstadt und der zentralen Lage im Reich, hat Bitterfels kaum etwas zu fürchten.
		Doch wurde in letzter Zeit immer häufiger von marodierenden Goblinbanden berichtet, welche einsame Reisende überfallen und ausrauben sollen. Diese kleinen gefährlichen Mistviecher kommen
		wahrscheinlich aus dem Altwald.</p>
		
		<p>Raigfurt liegt an einer Furt über den Oberraig, kurz vor der Mündung in den Kynraigsee. Es ist nur eine kleine Ortschaft, deren Bewohner sich vor allem auf die Reisenden
		zwischen Rodach und Neremund spezialisiert haben. Trotzdem gibt es noch einige Gehöfte, Fischer die den Kynraig befischen sowie Holzfäller und Jäger, welche in den nahe
		gelegenen Bruchwald gehen. Gefahren kommen ausschließlich aus dem gebirgigen Bruchwald, nördlich des Raigs.</p>
	</div>
