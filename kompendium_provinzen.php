<div class="row">
	<div class="span7">
		<p>Neremund ist der Sitz des Grafen in der Ostmark am Raig. Die Haupteinnahmequelle Neremunds ist Schafswolle für Kleidung und die etwas weiter nördlich 
		gelegenen Kupferminen. Auch der Weinanbau hat hier Tradition, auch wenn dieser nicht so süß und lieblich ist wie der Trakländische. In Neremund sind auch öfters 
		zwergische Grenzläufer unterwegs, also solche die ihre unterirdische Festung verlassen haben um zu handeln. Meistens liefern diese ihr berühmtes Zwergenbier und besondere 
		Waffen, und erhalten dafür Stoffe, manchmal Wein, meistens aber Klingende Münzen.</p>
		
		<p>Das Kloster ist dem Gott Thero geweiht und liegt in den letzten Ausläufern des Dunpadberges. Es wird berichtet, das Aeder persönlich vor 13.017 Jahren an 
		dieser Stelle die Schrift von den Göttern empfangen hatte. Das Kloster selber wurde aber erst 10.396 ae. von einem Wandermönch namens Batolo an dieser geheiligten Stelle 
		gegründet. Die Mönche des Thero sind gute Kelterer und Bierbrauer. Wirklich berühmt ist das Kloster jedoch für seine Bibliothek, gefüllt mit den Chroniken des Landes. Das 
		letzte schlimme Ereignis war der große Brand vor nun 73 Jahren, bei der, den Göttern sei dank, die Chroniken fast vollständig gerettet werden konnten.</p>
	</div>

	<div class="span7">
		<p>Holmstedt ist die nördlichste Siedlung des Imperiums. Das Städtchen Holmstedt ist direkt in der Nähe des gleichnamigen Passes durch die 
		Nebelberge. Das Passieren dieses Passes ist kaum von Bedeutung, da die dahinter liegenden Gebiete ein normales Handelsgeschäft zu risikoreich machen, weshalb lieber 
		auf den Seeweg in Richtung Keldon ausgewichen wird. Das einzige Produkt aus dieser Region ist warmer Pelz von Bergtieren, die beinahe mit denen aus Keldon konkurrieren 
		können. Das Leben hier ist recht gefährlich, da von zeit zu zeit Wildorkstämme auf die Idee kommen, diesen Weg ins Imperium zu nehmen oder einige Eisriesen von ihrem 
		Berg herunter steigen. Auch ist die Nähe zur Sturmspitze ein weiterer Grund, weswegen sich hier nur wenige Menschen niederließen.</p>
		
		<p>Hasburg liegt ähnlich wie Bitterfels sehr zentral im Imperium und ist ähnlich landwirtschaftlich geprägt. Es wird sowohl Getreide angebaut, als auch Rinder 
		und Schafe gezüchtet. Die Burg, die Hasburg im Namen trägt, existiert schon seit längerem nicht mehr und wurde schon während den ersten Thrakerkriegen geschliffen.</p>
	</div>
