		<h3>Münzen</h3>
	</div>
</div>
	
<div class="row">
	<div class="span5">
		<p>Die Münzen des Heiligen Reiches werden vom Kaiser autorisierten Münzereien in den Hauptstädten der fünf Reiche geprägt. In vielen Städten des Reiches gibt es Wechselstuben bei denen die verschiedenen Münzen des Reiches und auch ausländische Münzen gegen ein kleines Entgelt umgetauscht werden können.</p>
		<p>Die Münze mit dem geringsten Wert ist der Groschen, eine kleine aus Bronze gefertigte Münze, deren eine Seite mit einem Lorbeerkranz und die andere mit einem Baum verziert. Zehn Groschen werden gegen einen silbernen Heller getauscht. Heller sind auf einer Seite mit einer offenen Hand und der Rückseite mit dem Wappen der Länderei in der sie geprägt wurden versehen. Außerdem gibt es noch die Kreuzer, eine sehr flache silberne Münze, der auf beiden Seiten ein Kreuz geprägt wurde. Ein Kreuzer entspricht einem viertel Heller. Die erste goldene Münze ist ein Reichsgulden. Ein Reichsgulden Trägt das Portrait des Kaisers und die Inschrift „Kaiser des Heiligen Reiches – Von den Göttern erhoben“ auf der vorder und einen aufgerichteten Goldgreif auf der Rückseite. Ein Reichsgulden hat den Wert von zehn Hellern. In der Gossensprache hat sich für Reichsgulden auch der Ausdruck „Kopf“, aufgrund des abgebildeten Portraits, eingebürgert. Die größte und auch wertvollste Münze ist ein aus 30 Gramm Gold geprägter Batzen. Auf der Vorderseite ist das Wappen des Kaisers welches von einem aufgerichteten Löwen zur linken und einem Greifen zur rechten gehalten wird. Die Rückseite ziert das Abbild des Kaisers in voller Rüstung und Gezogenem Schwert auf seinem Thron. Ein Batzen hat den Wert von Hundert Hellern.</p>
	</div>
	<div class="span5">
		<p>Neben den Münzen wird außerdem auch mit Goldbarren, die ein Gewicht von 200 Gramm haben, gehandelt und diese als Zahlungsmittel angenommen. Selten werden auch Reichsverschreibungen ausgegeben. Dies sind gesiegelte Urkunden die dem Besitzer formell ein Stück Land im Wert von 100 Batzen überschreibt und nur von Kaiser und seinen Fürsten ausgegeben werden können. Diese beiden Zahlungsarten werden jedoch nur von Herrschern und sehr Wohlhabenden Händlern benutzt.</p>
		<p>In der südlichen Siedlung Belinga wird zudem gerne direkt mit Sumpfkraut bezahlt. Diese werden zu kleinen Kugeln im Wert von 5 Hellern gerollt und als „Murmeln“ bezeichnet.</p>
		<p>Das Prägen von Münzen aus Edelmetallen ist ein Recht, welches nur vom Hochkaiser verliehen werden kann. Derzeit gibt es fünf solcher Münzstätten im Reich. Jeweils eine in Bram, Erkester, Neremund, Boerlangen und eine in Raigfurt. Zunächst  werden nach exaktem Gewicht Rohlinge aus den Metallen gegossen. Anschließend wird die vorder sowie Rückseite mit einem Stempel hinein geschlagen und gehen dann in die gut gesicherten Schatzkammern des Imperators oder der Fürsten.</p>
	</div>	
</div>

<div class="row">
