		<p>Für den Fall, dass eine Person kein Geld mehr hat, gibt es die Möglichkeit sich welches gegen Zinsen zu leihen, oder etwas Wertvolles zu verpfänden. Ein pfandleier wird jedoch niemals mehr als die Hälfte des tatsächlichen Wertes eines Gegenstandes auszahlen. Kann man diesen Wert nicht in einer ausgemachten Frist zurückzahlen, geht das Wertstück in den Besitz des Pfandleihers über. Hat man auch nichts was man als Pfand hinterlegen kann, oder will man sich von keinem seiner Besitztümer trennen, muss man sein Glück bei einem Darlehensgeber versuchen.</p>
