		<h3>Steuern und Zölle</h3>
	</div>
</div>
	
<div class="row">
	<div class="span5">
		<p>Steuern werden erhoben um mit diesen Einnahmen die Sicherheit des Landes zu gewährleisten, Gebäude von öffentlichem Interesse zu errichten, Straßen und Brücken in stand zu setzen, den Bedürftigen zu helfen, sowie den Lebensunterhalt der Regenten zu sichern. Dabei Besteuert der Hochkaiser seine Fürsten, je nach Größe und Güte der ihnen zugesprochenen Ländereien. Eine Obergrenze für diese Steuern gibt es theoretisch nicht.</p>
	</div>
	<div class="span5">
		<p>Den Fürsten ist es gestattet ihre Freiherren, Lehnsvasallen und freie Städte zu besteuern. Zusätzlich werden Zölle auf Reichsstraßen und Brücken von den Fürsten selbst erhoben. Eine zusätzliche direkte Einnahmequelle für die Fürsten sind Jagd und Minenrechte, welche in unverlehnten Berg oder Waldregionen für Geld zu erwerben sind. Zölle dürfen einen zehnt der Warenwerte nicht übersteigen. Werden keine Waren zum Handeln mitgeführt darf der Wegezoll pro Kopf höchstens 5 Heller betragen, als Brückenzoll nicht mehr als 10. Zur Steuern darf nicht mehr als ein Drittel des gesamten Vermögens in einem Jahr eingezogen werden. Steuern sind entweder in Geld oder in Arbeit abzuleisten.</p>
	</div>	
</div>

